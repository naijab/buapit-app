package th.ac.buapit.app.Model;

public class PersonItem {

    public String personName;
    public String personPosition;
    public String personFaction;
    public String personTel;

    public String getPersonName() {
        return personName;
    }

    public void setPersonName(String personName) {
        this.personName = personName;
    }

    public String getPersonPosition() {
        return personPosition;
    }

    public void setPersonPosition(String personPosition) {
        this.personPosition = personPosition;
    }

    public String getPersonFaction() {
        return personFaction;
    }

    public void setPersonFaction(String personFaction) {
        this.personFaction = personFaction;
    }

    public String getPersonTel() {
        return personTel;
    }

    public void setPersonTel(String personTel) {
        this.personTel = personTel;
    }
}
